function Buttons() {
  const [openIndex, setOpenIndex] = useState(null);
  const handleClick = (event) => {
    const button = event.target.closest("button"); 
    if (button) {
      const index = button.dataset.index; 
      setOpenIndex(openIndex === index ? null : index); 
    }
  };

  return (
    <div className="w-full flex flex-col md:gap-10 gap-5 font-[Anglecia]" onClick={handleClick}>
      {buttons.map((button, i) => (
        <div key={i}>
          <button
            data-index={i} 
            className={`flex justify-between items-center  text-left w-full text-2xl font-normal leading-8 ${
              openIndex == i ? '' : 'text-[#de965f]'
            }`}
          >
            <span>{button.label}</span>
            <span>{openIndex == i ? <FaMinus /> : <FaPlus />}</span>
          </button>
          
          <AnimatePresence>
            {openIndex == i && (
              <motion.div
                initial={{ scaleY: 0 }}
                animate={{ scaleY: 1 }}
                exit={{ scaleY: 0 }}
                transition={{ duration: 0.3, ease: "easeInOut" }}
                className="w-full mt-2 text-lg "
                style={{ transformOrigin: "top" }}
              >
                {button.description} 
              </motion.div>
            )}

          </AnimatePresence>

        </div>
      ))}
    </div>
  );
}

export default Buttons;